DROP TABLE IF EXISTS appliance_models;
DROP TABLE IF EXISTS brands;

CREATE TABLE brands (
  brand_id   SERIAL PRIMARY KEY,
  brand_name VARCHAR(100) NOT NULL UNIQUE
);

CREATE TABLE appliance_models (
  model_id        SERIAL PRIMARY KEY,
  brand_id        INTEGER NOT NULL REFERENCES brands(brand_id) ON DELETE CASCADE,
  appliance_type  VARCHAR(50) NOT NULL,
  model_name      VARCHAR(150) NOT NULL,
  wattage_w       INTEGER NOT NULL,
  created_at      TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  UNIQUE (brand_id, appliance_type, model_name)
);

CREATE INDEX idx_type ON appliance_models(appliance_type);
CREATE INDEX idx_type_brand ON appliance_models(appliance_type, brand_id);

